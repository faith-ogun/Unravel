"""Assemble the backtest metrics into a writeup-ready table (task #4).

Runs the backtest over the synthetic cohort and emits a Markdown metric table
(detection precision / recall / F1 / specificity / FPR + confusion, and the
action safety-floor) to eval/metrics.md, plus eval/metrics.json for programmatic
use. Honest by construction: detection is the real, largely non-circular measure;
the action floor is reported as a safety regression check, with the live LLM
Adjudicator (scripts/run_adjudication.py) cited as the independent decision check.

Run:
  cd backend && PYTHONPATH=. .venv/bin/python eval/metrics_report.py
"""

from __future__ import annotations

import json
import sys
from datetime import date
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
import backtest  # noqa: E402

OUT_MD = Path(__file__).resolve().parent / "metrics.md"
OUT_JSON = Path(__file__).resolve().parent / "metrics.json"


def _table(rows: list[tuple[str, str]]) -> str:
    out = ["| Metric | Value |", "|---|---|"]
    out += [f"| {k} | {v} |" for k, v in rows]
    return "\n".join(out)


def render(report: dict) -> str:
    d, a, c = report["detection"], report["action"], report["cohort_counts"]
    cols = ["actionable", "withhold", "reassure", "ethics"]
    conf = a["confusion"]
    header = "| expected \\ predicted | " + " | ".join(cols) + " |"
    sep = "|" + "---|" * (len(cols) + 1)
    conf_rows = [header, sep]
    for exp in cols:
        conf_rows.append(f"| **{exp}** | " + " | ".join(str(conf[exp][p]) for p in cols) + " |")

    md = f"""# Unravel Evaluation Metrics

*Generated by `eval/metrics_report.py` on {date.today().isoformat()}. Re-run to refresh.*

Validation cohort: **{c['total_patients']} synthetic FHIR patients** carrying variants
with real ClinVar reclassification history, scored by the backtest runner
(`eval/backtest.py`). Scenario mix: {c['by_scenario']}. Of the index patients,
**{c['underrepresented_ancestry']}/{c['index_patients']}** are of an ancestry
under-represented in AlphaMissense's training data.

## Detection (the Watcher, real `detect_reclassifications`)

**Read this honestly.** A reclassification crosses an ACMG category boundary *by
definition*, and `detect_reclassifications` is a deterministic category diff, so
flagging the positive cases is correct by construction. The P/R/F1 below is a
**regression check** that the diff is wired correctly, NOT a measurement of skill;
do not headline it as accuracy.

{_table([
    ("Precision / Recall / F1", f"{d['precision']} / {d['recall']} / {d['f1']} (by construction)"),
    ("Confusion (TP / FP / FN / TN)", f"{d['tp']} / {d['fp']} / {d['fn']} / {d['tn']}"),
])}

The non-tautological detection test is **specificity on hard negatives**: cases
where the ClinVar *text* changes but the *category* does not (e.g. "Likely
pathogenic" -> "Pathogenic", or cosmetic benign churn), which must not be flagged.
This actually exercises the category-collapse logic.

{_table([
    ("Hard negatives (text change, same category)", f"{d['hard_negatives']['n']}"),
    ("False reclassifications raised", f"{d['hard_negatives']['false_positives']}"),
    ("Specificity on hard negatives", f"{d['hard_negatives']['specificity']}"),
])}

## Action safety-floor (deterministic, the auditable gate)

The floor encodes the Adjudicator's documented boundary (3+ star escalation acts;
1-star / conflicting withholds; benign reassures; deceased actionable routes to
ethics). Reported as a **safety regression check**: the safety-critical figures are
withhold-recall on the traps and the count of dangerous escalations. The live
Gemini Adjudicator is validated independently on the demo cases by
`scripts/run_adjudication.py`.

{_table([
    ("Withhold recall (1-star traps held)", f"{a['withhold_recall']} on {a['withhold_n']} traps"),
    ("Dangerous escalations (should-hold -> recontact)", f"{a['dangerous_escalations']}"),
    ("Action accuracy (vs labels, by construction)", f"{a['accuracy']} over {a['n_scored']} detected"),
])}

Action confusion matrix (rows = expected, cols = predicted):

{chr(10).join(conf_rows)}

## Honest framing for the writeup

- Do NOT headline the detection P/R/F1 = 1.0: it is true by construction
  (a deterministic category diff over constructed inputs), a regression check.
- Lead instead with the genuinely non-tautological results: **specificity on
  hard negatives** (the system ignores cosmetic ClinVar text changes), the
  **withhold-recall** and **zero dangerous escalations** safety properties, the
  **calibration** (anchors reproduced to 0.0001; predictor-alone insufficiency on
  31k real variants), and the **live Adjudicator** decision on the demo cases
  (`run_adjudication.py`), which is the only independent test of the moat.
- Describe the cohort as *synthetic patients carrying variants with real ClinVar
  reclassification history* (not real patient data, not clinically validated).
"""
    return md


def main() -> None:
    report = backtest.evaluate()
    OUT_MD.write_text(render(report))
    OUT_JSON.write_text(json.dumps(report, indent=2))
    d, a = report["detection"], report["action"]
    print(f"detection: precision={d['precision']} recall={d['recall']} f1={d['f1']} fpr={d['false_positive_rate']}")
    print(f"action:    withhold_recall={a['withhold_recall']} dangerous={a['dangerous_escalations']}")
    print(f"wrote {OUT_MD} and {OUT_JSON}")


if __name__ == "__main__":
    main()
